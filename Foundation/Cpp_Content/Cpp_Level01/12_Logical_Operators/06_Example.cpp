#include <iostream>

using std::cout;
using std::endl;

int main()
{
  cout<<"12 >= 12 => "<<(12 >= 12)<<"\n";//True = 1
  cout<<"12 > 7 => "<<(12 > 7)<<"\n";//True = 1
  cout<<"8 < 6 => "<<(8 < 6)<<"\n";//False = 0
  cout<<"8 == 8 => "<<(8 == 8)<<"\n";//True = 1
  cout<<"12 <= 12 => "<<(12 <= 12)<<"\n";//True = 1
  cout<<"7 == 5 => "<<(7 == 5)<<endl;//False = 0
  return 0;
}
