#include <iostream>

using std::cout;
using std::endl;

int main()
{
  short X{3}, Y{55};

  cout<<"X: "<<X<<"\nY: "<<Y<<"\n\n";

  X = Y + Y++ - ++X + X - ++Y + X++ + ++Y;// 116 = 55 + 55(+1) - 4 + 4 - 57 + 5 + 58
  /*
  X = 116
  Y = 58 
  */
  cout<<"X: "<<X<<"\nY: "<<Y<<"\n\n";
  
  Y = X++ + X + X - X + ++X - Y + ++Y + Y - X;// 293 = 116(+1) + 117 + 118 - 58 + 59 + 59 - 118 
  /*
  X = 118
  Y = 293
  */
  cout<<"X: "<<X<<"\nY: "<<Y<<"\n\n";

  X = Y++ - Y++ + ++Y + Y++;// 591 = 293(+1) - 294(+1) + 296 + 296(+1)
  /*
  X = 591
  Y = 297
  */
  cout<<"X: "<<X<<"\nY: "<<Y<<"\n\n";
  Y = X + Y++ - Y-- + ++X - X++ + X + Y--;// 1480 = 591 + 297(+1) - 298(-1) + 592 - 592(+1) + 593 + 297(-1)
  /*
  X = 593
  Y = 1480
  */
  cout<<"X: "<<X<<"\nY: "<<Y<<endl;
  return 0;
}
